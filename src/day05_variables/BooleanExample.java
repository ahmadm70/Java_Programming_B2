package day05_variables;

public class BooleanExample {
    public static void main (String [] args) {

        boolean isSleepy = true;    //We can't use capital letters
        boolean isHungry = false;   //We can't use YES or NO
        boolean isWeekend = false;  //We can't use 0 or 1
        boolean isWeekday = true;

        System.out.println("isSleepy = " + isSleepy);
        System.out.println("isHungry = " + isHungry);
        System.out.println("It is Weekend: " + isWeekend);
        System.out.println("It is Weekday: " + isWeekday);
    }
}
