package day07_relational_operators;

public class OperatorPractice {
    public static void main (String [] args) {

        int num = 10;
        num++;
        System.out.println(num);        //11

        num--;
        System.out.println(num);        //10

        System.out.println(num++);      //10
        System.out.println(num++);      //11
        System.out.println(++num);      //13
    }
}
